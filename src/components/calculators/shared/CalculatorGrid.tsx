export function CalculatorGrid({ calculators }: Props) {
  const language = useSettingsStore((s) => s.language)

  return (
    <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 xl:grid-cols-4 gap-4">
      {calculators.map((calc) => (
        <Link
          key={calc.id}
          href={`/calculator/${calc.slug}`}
          className="glass-card hover:border-primary transition group"
        >
          <div className="flex items-start gap-3">
            <div className="w-10 h-10 rounded-lg bg-primary/20 flex items-center justify-center text-primary">
              <CalculatorIcon name={calc.icon} />
            </div>
            <div className="flex-1 min-w-0">
              <h3 className="font-semibold text-sm group-hover:text-primary transition truncate">
                {getCalculatorName(language, calc.id)}
              </h3>
              <p className="text-xs text-muted line-clamp-2 mt-1">
                {getCalculatorDescription(language, calc.id)}
              </p>
            </div>
          </div>
          <div className="mt-3 flex flex-wrap gap-1">
            {calc.tags.slice(0, 3).map((tag) => (
              <span
                key={tag}
                className="text-[10px] px-2 py-0.5 rounded bg-muted/50 text-muted"
              >
                {getTagTranslation(language, tag)}
              </span>
            ))}
          </div>
        </Link>
      ))}
    </div>
  )
}

